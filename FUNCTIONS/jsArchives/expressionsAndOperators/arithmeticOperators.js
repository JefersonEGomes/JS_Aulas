// Operadores Aritiméticos

// multiplicação (*)
console.log(3 * 5)
console.log(3.2 * 5.5)
console.log('----------------------------------------')

// divisão (/)
console.log(12 / 2)
console.log('----------------------------------------')

// soma (+)
console.log(34 + 67)
console.log('----------------------------------------')

// subtração (-)
console.log(34 - 23)
console.log('----------------------------------------')

// resto da divisão (%)
let remainder
remainder = 11 % 9
console.log(remainder)
console.log('----------------------------------------')

// incremento (+'+)
let increment = 0
increment++
increment++
console.log(increment)

// incremento dentro do console.log
console.log(increment++)
console.log(increment)

// incremento dentro do console.log antes da variavel
console.log(++increment)
console.log('----------------------------------------')

// decremento (--)
let decrement = 0
decrement--
console.log(decrement)
console.log('----------------------------------------')

// exponencial (**)
console.log(3 ** 3)
console.log(2 ** 4)

